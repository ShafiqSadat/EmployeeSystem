    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jobName.getText().trim().isEmpty()|| jobSalary.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Name or salary is empty! ");
        }
        else if (redis.sismember("jobName", jobName.getText().trim())) {
            JOptionPane.showMessageDialog(null,"Job is already added in database!");
        }
        else{
        String name = jobName.getText().trim();
        String salary = jobSalary.getText().trim();
        redis.sadd("jobName",name+" "+salary);
        redis.sadd("jobSalary", salary);
        Job job = new Job(Double.parseDouble(salary),name);
        JOptionPane.showMessageDialog(null,"Done New job Saved!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

